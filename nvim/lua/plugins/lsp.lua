        "--clang-tidy",
        "--suggest-missing-includes",
        "--all-scopes-completion",
        "-j=4",
        "--inlay-hints",
        "--header-insertion-decorators",
    },
}

require('lspconfig').pyright.setup {
    on_attach = on_attach,
    flags = lsp_flags,
}
